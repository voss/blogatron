<h2>Spammers move to blogs</h2>
<a name="000411"></a>
<p>When I got <a href="http://verture.net/archives/2002/ind/blogger_hacked2610.html#000250">this</a>
comment on my posting on Blogger misfortune on friday, I was wondering
what the hell that was all about. A clue? To me? In which way had I
asked for one?</p>
<p>Anywho, <a href="http://netliv.net/">Winther</a> got the same comment to his <a href="http://www.netliv.net/arkiv/2002_10_01_arkiv.html#85602945">posting</a>
about bloggers outage. Odd, but probably just a nutcase trying to
convey a new meaning of the universe and hackers to the world of
weblogs.</p>
<p>Enter <a href="http://pages.sbcglobal.net/shanebonham/shane/">Shane</a>,
who did a bit of research. Turns out, that Mr. Comment left a trail all
over the blogdex list of blogs that wrote about bloggers hack incident.
Shane <a href="http://pages.sbcglobal.net/shanebonham/shane/2002_10_01_archive.html#85608083">figures</a> it's a vast conspiracy [well, sort of anyway].</p>
<p>Enter <a href="http://philringnalda.com/">Phil Ringnalda</a>, the digital magpie. He suffered a spam attack targeted at his comments-section of his <a href="http://moveabletype.org/">MT</a>-driven
weblog. Now, Phil is slick, so he stopped the culprit somewhere in the
beginning of his felony with some .htaccess magic and some IP-banning,
so Phil is safe, but the question is, what about the rest of us. Do we
